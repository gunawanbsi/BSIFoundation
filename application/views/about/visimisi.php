        <h1>VISI</h1>
        <p> BSI Foundation memiliki visi untuk konservasi yang sejalan dengan Tujuan Pembangunan Berkelanjutan dan berkontribusi 
        pada pembangunan berkelanjutan di Indonesia, dengan berfokus pada tata kelola sumber daya alam yang adil untuk menjamin manfaat 
        konservasi di masa depan : <br>
        <br>
        "Ekosistem dan keanekaragaman hayati Indonesia lestari, berkelanjutan dan terkelola secara adil, 
        untuk kesejahteraan generasi kini dan nanti" </p> <br>
        <br>
        <h1>MISI</h1>
        <p> Untuk mewujudkan visi ini, BSI Foundation menetapkan misi berikut ini: <br>
            Mewujudkan tata kelola ekosistem dan keanekaragaman hayati Indonesia yang adil dan berkelanjutan
            untuk kesejahteraan masyarakat Indonesia<br>
            <br>
            melalui:<br>
            <br>
            1. Mengimplementasikan dan mendorong praktik konservasi berdasarkan ilmu pengetahuan, inovasi, dan kearifan lokal;<br>
            2. Membangun koalisi dan kemitraan dengan masyarakat sipil, bekerja bersama pemerintah dan swasta untuk pembangunan berkelanjutan;<br>
            3. Mendorong nilai-nilai konservasi lingkungan hidup dengan meningkatkan kesadartahuan dan aksi konservasi di tengah masyarakat;<br>
            4. Mengadvokasi dan memengaruhi kebijakan dan mendorong penegakan hukum di institusi yang bertanggung jawab guna terlaksananya tata kelola lingkungan yang baik.
        </p>
